function extractId(url) {
  const params = new URL(url).searchParams;
  return params.get("id");
}

function buildPhoto(link, index, type, titlePrefix) {
  const id = extractId(link);

  return {
    id: `${type}-${index + 1}`,
    type,
    title: `${titlePrefix} ${index + 1}`,
    thumbnailUrl: `https://drive.google.com/thumbnail?id=${id}&sz=w300`,
    imageUrl: `https://drive.google.com/uc?export=view&id=${id}`,
    downloadUrl: `https://drive.google.com/uc?export=download&id=${id}`,
      previewUrl: `https://drive.google.com/thumbnail?id=${id}&sz=w1600`,
        shareUrl: `https://drive.google.com/file/d/${id}/preview`,


  };
}

